#include "../commun/includes/op.h"
#include "../corewar/includes/corewar.h"

void		ft_free_nc(t_a *a)
{
	int		i;

	ft_fruit(2, &a->nc.win_mem, &a->nc.win_info);
	i = -1;
	while (++i < a->num_of_player)
		ft_fruit(1, &a->nc.win_player[i]);
	endwin();
}
