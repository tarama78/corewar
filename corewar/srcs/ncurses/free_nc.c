#include <corewar.h>

void		ft_free_nc(t_a *a)
{
	int		i;

	delwin(a->nc.win_mem);
	delwin(a->nc.win_mem);
	i = -1;
	while (++i < a->num_of_player)
		delwin(a->nc.win_player[i]);
	endwin();
}
